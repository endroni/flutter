import 'dart:math';

void main() {
  // Usando a função sqrt da biblioteca math para calcular a raiz quadrada de 25
  var raizQuadrada = sqrt(25);
  print('A raiz quadrada de 25 é $raizQuadrada');

  // Usando a constante pi da biblioteca math para calcular a circunferência de um círculo
  var raio = 5;
  var circunferencia = 2 * pi * raio;
  print('A circunferência de um círculo com raio $raio é $circunferencia');
}
